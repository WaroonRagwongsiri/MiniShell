#include "libft.h"

int	ft_isalnum(int c)
{
	if (ft_isalpha(c) || ft_isdigit(c))
		return (1);
	else
		return (0);
}

// isalnum()
// checks for an alphanumeric character; it is equivalent 
// to (isalpha(c) || isdigit(c)).
