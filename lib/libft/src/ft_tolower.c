int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (c + 'a' - 'A');
	else
		return (c);
}

// RETURN VALUE
// 	The value returned is that of the converted letter, or  c
// 	if the conversion was not possible.